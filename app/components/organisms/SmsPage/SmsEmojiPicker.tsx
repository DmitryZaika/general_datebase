export interface SmsEmojiPickerProps {
  onPick: (emoji: string) => void
  disabled?: boolean
}

export function SmsEmojiPicker(props: SmsEmojiPickerProps) {
  const [open, setOpen] = useState(false)

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <button
          type='button'
          aria-label='Insert emoji'
          disabled={props.disabled}
          className='shrink-0 h-11 w-11 inline-flex items-center justify-center rounded-md text-slate-500 hover:text-slate-700 hover:bg-slate-100 disabled:opacity-50 disabled:cursor-not-allowed transition-colors'
        >
          <Smile size={18} />
        </button>
      </PopoverTrigger>
      <PopoverContent align='end' side='top' className='w-auto p-2'>
        <div className='max-h-72 overflow-y-auto'>
          <div className='grid grid-cols-8 gap-1'>
            {EMOJIS.map(e => (
              <button
                key={e}
                type='button'
                onClick={() => {
                  props.onPick(e)
                  setOpen(false)
                }}
                className='h-8 w-8 flex items-center justify-center text-lg rounded hover:bg-slate-100 transition-colors'
                aria-label={`Insert ${e}`}
              >
                {e}
              </button>
            ))}
          </div>
        </div>
      </PopoverContent>
    </Popover>
  )
}
